package cz.incad.cdk.cdkharvester;

public class CDKReplicationException extends Exception {

	public CDKReplicationException() {
		super();
	}

	public CDKReplicationException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public CDKReplicationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CDKReplicationException(String arg0) {
		super(arg0);
	}

	public CDKReplicationException(Throwable arg0) {
		super(arg0);
	}
}
